export function normalizeForLookup(text: string) {
  return text
    .replace(/[’]/g, "'")
    .replace(/[“”]/g, '"')
    .replace(/\s+/g, " ")
    .trim();
}

function preserveWhitespace(source: string, translated: string) {
  const leading = source.match(/^\s*/)?.[0] ?? "";
  const trailing = source.match(/\s*$/)?.[0] ?? "";
  return `${leading}${translated}${trailing}`;
}

export function translateString(text: string, locale: AppLocale) {
  if (locale !== "ar") return text;

  const normalized = normalizeForLookup(text);
  if (!normalized) return text;

  const exact = exactArabicTranslations.get(normalized);
  if (exact) {
    return preserveWhitespace(text, exact);
  }

  for (const translation of arabicPatternTranslations) {
    const match = normalized.match(translation.pattern);
    if (match) {
      return preserveWhitespace(text, translation.replace(match));
    }
  }

  return text;
}
